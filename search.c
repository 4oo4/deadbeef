static GtkWidget *searchwin = NULL;

void
search_start (void) {
    if (!searchwin) {
        searchwin = create_searchwin ();
    }
    gtk_widget_show (searchwin);
    gtk_window_present (GTK_WINDOW (searchwin));
}

